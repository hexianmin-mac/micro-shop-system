package org.hxm.microsys.core.entity;

public class User {


    private int userId;
    private String userName;
    private int sex;
    private int age;
    private String phone;
    private String address;
    private int type;
    private int status;


    public int getUserId() {
        return userId;
    }


    public String getUserName() {
        return userName;
    }

    public int getSex() {
        return sex;
    }

    public int getAge() {
        return age;
    }

    public String getPhone() {
        return phone;
    }

    public String getAddress() {
        return address;
    }

    public int getType() {
        return type;
    }

    public int getStatus() {
        return status;
    }


}
